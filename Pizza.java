package Pizzafactory;

public class Pizza {
String name;
double price;
int size;
String crusttype;
String toppinglist[];
public Pizza(String name, double price, int size, String crusttype, String[] toppinglist) {
	super();
	this.name = name;
	this.price = price;
	this.size = size;
	this.crusttype = crusttype;
	this.toppinglist = toppinglist;
}

}
